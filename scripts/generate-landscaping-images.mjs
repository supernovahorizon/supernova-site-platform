async function download(url) {
  const response = await fetch(url);
  if (!response.ok) throw new Error(`Failed to download ${url}: ${response.status}`);
  return Buffer.from(await response.arrayBuffer());
}

async function writeVariants(buffer, basePath) {
  await mkdir(dirname(basePath), { recursive: true });
  await sharp(buffer).jpeg({ quality: 82, mozjpeg: true }).toFile(`${basePath}.jpg`);
  await sharp(buffer).webp({ quality: 80 }).toFile(`${basePath}.webp`);
  await sharp(buffer).avif({ quality: 65 }).toFile(`${basePath}.avif`);
}

async function processSet(dir, images) {
  let fallback = null;
  for (const [name, url] of Object.entries(images)) {
    const basePath = join(root, dir, name);
    console.log(`→ ${name}`);
    try {
      const buffer = await download(url);
      fallback = buffer;
      await writeVariants(buffer, basePath);
    } catch (error) {
      console.warn(`  skipped ${name}: ${error.message}`);
      if (fallback) await writeVariants(fallback, basePath);
    }
  }
}

